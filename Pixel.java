public class Pixel {

    private short red;
    private short blue;
    private short green;

    public Pixel(short red, short green, short blue){
        this.red = red;
        this.blue = blue;
        this.green = green;
    }

    public short getRed() {
        return red;
    }

    public short getGreen() {
        return green;
    }

    public short getBlue() {
        return blue;
    }
}
